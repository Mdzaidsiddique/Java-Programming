package com.p2_dt;

public class WrapperClass {

/*  - class representation of primitive data types
    - for each wrapper class java provided constant variable, MIN_VALUE & MAX_VALUE to identify min & max value
    - these variables comes from java.lang package

    java.lang.Byte;
    java.lang.Short;
    java.lang.Integer;
    java.lang.Long;
    java.lang.Float;
    java.lang.Double;
    java.lang.Character;
    java.lang.Boolean; (Boolean.MIN_VALUE & Boolean.MAX_VALUE -> error)

    - there are many other uses of wrapper classes (autoboxing, unboxing, null handling, generics, collections, parsing string, etc)
*/

    public static void main(String[] args) {
        System.out.println(Integer.MAX_VALUE);
    }

}

